#include <bits/stdc++.h>
using namespace std;



int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int n;
    cin>>n;

    vector<int> a(n+1), b(n+1);
    for (int i = 1; i <= n; ++i){
        cin>>a[i]>>b[i];
    }

    int m;
    cin>>m;
    vector<string> s(m);
    for (int i = 0; i < m; ++i){
        cin>>s[i];
    }

    bool flag[20][20][27] = {};
    for (string &t : s){
        int l = t.length();
        for (int i = 1; i <= l; ++i){
            flag[l][i][t[i-1] - 'a'] = true;
        }
    }

    for(int i = 0; i < m; ++i){
        if (s[i].length() != n){
            cout<<"No"<<endl;
            continue;
        }

        bool ok = true;
        for (int j = 1; j <= n; ++j){
            int target = s[i][j-1] - 'a';
            if (!flag[a[j]][b[j]][target]){
                ok = false;
                break;
            }
        }
        if (ok){
            cout<<"Yes"<<endl;
        }
        else{
            cout<<"No"<<endl;
        }
    }
    
    return 0;
}

