#include <bits/stdc++.h>
using namespace std;



int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    string s, t;
    cin>>s>>t;
    int n = s.length();
    int m = t.length();

    vector<vector<int> > next(n+10, vector<int> (27, INT_MAX/2));
    for (int i = n; i >= 1; --i){
        next[i] = next[i+1];
        next[i][s[i-1] - 'a'] = i;
    }

    long long ans = 0;
    for (int i = 1; i <= n; ++i){
        int cur = i;
        bool flag = true;
        int tail = -1;
        for (int j = 0; j < m; ++j){
            int c = t[j] - 'a';
            int pos = INT_MAX / 2;
            if (cur <= n){
                pos = next[cur][c];
            }
            if (pos > n){
                flag = false;
                break;
            }
            tail = pos;
            cur = pos + 1;
        }
        if (!flag) ans += (n - i + 1);
        else ans += (tail - i);
    }

    cout<<ans<<endl;
    
    return 0;
}

