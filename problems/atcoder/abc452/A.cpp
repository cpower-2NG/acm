#include <bits/stdc++.h>
using namespace std;



int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    pair<int, int> a[5] = {{1, 7}, {3, 3}, {5,5}, {7,7}, {9,9}};
    pair<int, int> in;
    cin>>in.first>>in.second;
    for (int i = 0; i < 5; ++i){
        if (in == a[i]){
            cout<<"Yes"<<endl;
            return 0;
        }
    }
    cout<<"No"<<endl;

    return 0;
}

