#include <bits/stdc++.h>
using namespace std;

const int mod = 998244353;

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int n, m;
    cin>>n>>m;
    vector<long long> a(n+1), b(m+1);
    for (int i = 1; i <= n; ++i) cin>>a[i];
    for (int i = 1; i <= m; ++i) cin>>b[i];
    
    vector<long long> pref(n+1, 0);
    for (int i = 1; i <= n; ++i){
        pref[i] = (pref[i-1] + a[i]) % mod;
    }

    long long suma = 0;
    for (int i = 1; i <= n; ++i){
        suma = (suma + a[i] * i) % mod;
    }

    long long ans = 0;
    for (int i = 1; i <= m; ++i){
        long long t = 0;
        if(i <= n){
            int mx = n / i;
            for (int j = 1; j <= mx; ++j){
                int l = i * j;
                int r = min(n, (j + 1) * i - 1);
                long long sum = (pref[r] - pref[l-1]) % mod;
                if (sum < 0) sum += mod;
                t = (t + j * sum) % mod;
            }
        }

        long long sumb = (suma - t * i) % mod;
        if (sumb < 0) sumb += mod;
        ans = (ans + b[i] * sumb) % mod;
    }

    cout<<ans<<endl;

    return 0;
}

