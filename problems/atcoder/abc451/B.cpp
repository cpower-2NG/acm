#include <bits/stdc++.h>
using namespace std;


int main() {
    int n, m;
    cin>>n>>m;
    vector<vector<int> > a(2, vector<int>(m+1,0));
    for (int i = 0; i < n; ++i){
        int x, y;
        cin>>x>>y;
        a[0][x]++;
        a[1][y]++;
    }
    for (int i = 1; i <= m; ++i){
        cout<<a[1][i] - a[0][i]<<endl;
    }
    
    return 0;
}

