#include <bits/stdc++.h>
using namespace std;



int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    vector<long long > a, two;
    for (int i = 1; i < 1e9; i<<=1){
        two.push_back(i);
    }

    vector<int> len(two.size());
    vector<long long> p(11, 1);
    for (int i = 1; i <= 10; ++i){
        p[i] = p[i-1] * 10;
    }
    for (int i = 0; i < two.size(); ++i){
        len[i] = to_string(two[i]).size();
    }

    vector<int> ans;
    function<void(long long)> dfs = [&](long long cur){
        for (int i = 0; i < two.size(); ++i){
            long long next = cur * p[len[i]] + two[i];
            if (next <= 1e9){
                ans.push_back(next);
                dfs(next);
            }
        }
    };

    dfs(0);
    sort(ans.begin(), ans.end());
    ans.erase(unique(ans.begin(), ans.end()), ans.end());

    int n;
    cin>>n;
    cout<<ans[n-1]<<endl;

    return 0;
}

