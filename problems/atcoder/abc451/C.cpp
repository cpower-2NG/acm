#include <bits/stdc++.h>
using namespace std;


int main() {
    int q;
    cin>>q;
    priority_queue<long long, vector<long long>, greater<> > pq;
    for (int i = 0; i < q; ++i){
        int sign;
        long long h;
        cin>>sign>>h;
        if (sign == 1){
            pq.emplace(h);
        }
        else {
            while(!pq.empty() && pq.top() <= h){
                pq.pop();
            }
        }
        cout<<pq.size()<<endl;
    }
    
    
    return 0;
}

